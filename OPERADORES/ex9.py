# Exercício 9 — Operadores de Atribuição Composta
numero = 10
print(f"Valor inicial: {numero}")

numero += 5
print(f"Após += 5: {numero}")

numero *= 2
print(f"Após *= 2: {numero}")

numero -= 8
print(f"Após -= 8: {numero}")

numero /= 3
print(f"Após /= 3: {numero}")

numero %= 4
print(f"Após %= 4: {numero}")

numero **= 2
print(f"Após **= 2: {numero}")
